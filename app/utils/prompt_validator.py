"""
Валидатор промптов для фильтрации "ленивых" запросов
Блокирует короткие бессмысленные фразы, экономя GPU и баланс пользователя
"""
import re
import emoji

# Список триггерных фраз (короткие ленивые запросы)
LAZY_TRIGGERS = [
    "сделай красиво", "сделай красивое", "красивое фото","сделать красиво","Красивый образ",
    "красиво", "красивое","Что-то интересное",
    "на твой вкус", "на ваш вкус",
    "как лучше", "как хочешь",
    "любое", "любой",
    "улучши", "улучшить",
    "обработай", "обработка",
     "фото", "фоточку",  # ✅ Вернули
    "что-нибудь", "что нибудь",
    "шедевр","бесплатно",
    "удиви меня",
    "просто фото"
    "вау", "Вау"
    "красота", "Красота",
    "сделаете",
    "Что-то интересное",
]

def clean_text(text: str) -> str:
    """
    Очищает текст от эмодзи, лишних пробелов, приводит к нижнему регистру
    
    Args:
        text: Исходный текст
        
    Returns:
        Очищенный текст
    """
    # Убираем эмодзи
    text = emoji.replace_emoji(text, replace='')
    
    # Убираем лишние пробелы и приводим к lower
    text = ' '.join(text.lower().split())
    
    return text

def is_lazy_prompt(text: str) -> bool:
    """
    Проверяет, является ли запрос "ленивым" (пустым/бессмысленным)
    
    Логика:
    1. Очищаем текст от эмодзи и пробелов
    2. Если текст длинный (>3 слов) - пропускаем (это нормальный промпт)
    3. Если короткий И ТОЧНО СОВПАДАЕТ с триггером - блокируем
    
    Args:
        text: Текст запроса пользователя
        
    Returns:
        True если запрос нужно заблокировать, False если пропустить
    """
    if not text:
        return True  # Пустой текст блокируем
    
    # Очищаем
    cleaned = clean_text(text)
    
    if not cleaned:
        return True  # Если после очистки пусто - блокируем
    
    # Считаем слова
    word_count = len(cleaned.split())
    
    # Если длинный текст (>3 слов) - всегда пропускаем
    if word_count > 3:
        return False
    
    # ✅ ТОЧНОЕ СОВПАДЕНИЕ: блокируем только если текст ПОЛНОСТЬЮ равен триггеру
    return cleaned in LAZY_TRIGGERS